package com.doctypenepal;

public class WrapperClass {
    public WrapperClass(){
        // Wrapper class = provides a way to use primitive data types as
        // reference data type

        // Primitive         Wrapper
        //----------        ----------
        // boolean          Boolean
        // char             Char
        // int              Int
        // double           Double

        // autoboxing = automatic conversion that the java compiler makes between primitive data type and corresponding wrapper class
        // unboxing = autoboxing vice-versa

        Boolean a = true;
        Integer c = 123;
        Double d = 3.14;
        String e = "Batman";
    }
}
